#ifndef _CONFIG_H_
#define _CONFIG_H_

#include <avr/io.h>
#include <avr/interrupt.h>

// porject specific define of system timer ISR
#define SYSTEM_TIMER_ISR TIMER1_COMPA_vect
#define RESCHED_LOCAL_ISR INT0_vect

#ifndef __ASSEMBLER__

#define INLINE __attribute__((__always_inline__))

#define SYS_TMR_ISR_ATTR __attribute__ (( signal, naked ))
#define RLOC_ISR_ATTR __attribute__ (( signal, naked ))

// data types
#define BITS_IN_INDEX_T (8)
typedef unsigned char index_t;

typedef unsigned char prio_t;
typedef char stack_t;
typedef void (* code_t)(void);
typedef unsigned char flag_t;
typedef unsigned short count_t;
typedef unsigned short timer_t;
typedef volatile char lock_t;
typedef unsigned char bool_t;

// use o(1) search method 2 find queues heads
#define CONFIG_USE_O1_SEARCH

/*!
 \brief BuguRTOS interrupt attribute.

 U must use only this to declare interrupts.

 Example: BURURT_INTERRUPT void INT0_vect(void){}
 */
#define BUGURT_INTERRUPT __attribute__ (( signal ))

//=================================================================
// project stecific stuff
//=================================================================
static inline void start_scheduler( void ){
    TIMSK |= 0x10;
    EIMSK |= 0x01;
}
static inline void stop_scheduler( void ){
    TIMSK &= ~0x10;
    EIMSK &= ~0x01;
}
/*
#define CONFIG_MP
#define MAX_PROCESSORS (2)
#define CONFIG_USE_ALB
*/
#endif //__ASSEMBLER__

#endif // _CONFIG_H_
