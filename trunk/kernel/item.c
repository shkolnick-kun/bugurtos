#include "../include/bugurt.h"
//Методы элемента 2-связного списка
//инициация элемента
void item_init(item_t *item)
{
    item->prev = item;
    item->next = item;
}
//Вставить элемент
void item_insert(item_t *item, item_t *head)
{
    // "хвост" списка
    item_t * tail;
    tail = head->prev;
    // Вставляем элемент между "головой" и "хвостом"
    item->prev = tail;
    item->next = head;

    head->prev = item;
    tail->next = item;
}
//Вырезать элемент
void item_cut( item_t *item )
{
    item_t * prev;
    item_t * next;
    prev = item->prev; //Предыдущий элемент
    next = item->next; //Следующий элемент

    next->prev = prev;
    prev->next = next;

    item->prev = item;
    item->next = item;
}
