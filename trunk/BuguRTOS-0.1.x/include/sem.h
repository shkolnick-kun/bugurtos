#ifndef _SEM_H_
#define _SEM_H_
/*!
 \file sem.h
 \brief Semaphore definition & control functions.

 Semaphores R used in common resource control.
 The main usage of semaphores is mutual exclusion,
 when only one process is permited 2 access some common resource (data structure 4 example) 4 a relatively long time.
 Also if U want 2 limit a number of proceesses accessing some common resource (code) simultaneously, then U have 2 use semaphores.
*/
/*!
 \struct _sem
 \brief Semaphore structure.
 \typedef sem_t
 \brief Semaphore type.
*/
typedef struct _sem{
    lock_t value;           //!< Count of possible locks.
    proc_queue_t rt_queue;  //!< RT process wait queue.
    proc_queue_t gp_queue;  //!< General purpose process wait queue.
} sem_t;
/*!
 \brief Semaphore initialization function.

 Initiates the semaphore.

 If value is 1, then this semaphore will B binary semaphore or mutex.
 \warning If U locked a binary semaphore, then U must unlock it B4 U lock it next time within the same process!
 \warning If U unlocked a binary semaphore, then U must lock it B4 U unlock it next time within the same process!

 If value is greater than 1 then this semaphore will B counting. A counting semaphore can B locked or unlocked more than once within the same process.
 \warning The count of consequent semapthore locks within one process must B equal 2 the count of of consequent semapthore unlocks within the same process!
 \warning Do not lock a couting semaphore more times than initial semaphore value was!

*/
void sem_init(
                sem_t * sem, //!< A pointer 2 a semaphore.
                lock_t value //!< Initioal value of a semaphore.

                );
/*!
 \brief Try 2 lock a semaphore.

 Try 2 lock a semaphore, returns true if sucseeded.
*/
bool_t sem_try_lock(
                    sem_t * sem //!< A pointer 2 a semaphore.
                    );
/*!
 \brief Try 2 lock a semaphore.

 Try 2 lock a semaphore, returns true if sucseeded.
 If failed stops a caller process inserts it 2 a correspondent wait queue and returns false.
 \warning Do not call within a critical section or interrupt!
*/
bool_t sem_lock(
                sem_t * sem //!< A pointer 2 a semaphore.
                );
/*!
 \brief Unlock a semaphore.

 Unlockes a semaphore.
 \warning Do not call within a critical section or interrupt!
*/
void sem_unlock(
                sem_t * sem //!< A pointer 2 the semaphore
                );

#endif // _SEM_H_
