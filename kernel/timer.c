#include "bugurt.h"
/* ADLINT:SF:[W0422] NULL */
/*===========================================================================*/
void bgrt_priv_clear_timer(bgrt_tmr_t * t)
{
    BGRT_ASSERT(t, "The #t must not be NULL!");

    BGRT_CRIT_SEC_ENTER(); /* ADLINT:SL:[W0425] several def/expr*/
    BGRT_SPIN_LOCK(&bgrt_kernel.timer);

    *t = bgrt_kernel.timer.val;

    BGRT_SPIN_FREE(&bgrt_kernel.timer);
    BGRT_CRIT_SEC_EXIT();
}
/*===========================================================================*/
bgrt_tmr_t bgrt_priv_timer(bgrt_tmr_t t)
{
    bgrt_tmr_t ret;
    BGRT_CRIT_SEC_ENTER(); /* ADLINT:SL:[W0425] several def/expr*/
    BGRT_SPIN_LOCK(&bgrt_kernel.timer);

    ret = (bgrt_tmr_t)bgrt_kernel.timer.val - (bgrt_tmr_t)t; /* ADLINT:SL:[W1052] OVF*/

    BGRT_SPIN_FREE(&bgrt_kernel.timer);
    BGRT_CRIT_SEC_EXIT();

    return ret;
}
/*===========================================================================*/
#ifndef BGRT_CONFIG_TEST
#   define  _BGRT_TMR_YIELD() (void)bgrt_sched_proc_yield()
#else
#   define  _BGRT_TMR_YIELD() do{}while(0)
#endif /*BGRT_CONFIG_TEST*/

void bgrt_wait_time(bgrt_tmr_t time)
{
    bgrt_tmr_t tmr;
    BGRT_CLEAR_TIMER(tmr); /* ADLINT:SL:[W0459] does not assign*/
    do
    {
        _BGRT_TMR_YIELD();
    } while (BGRT_TIMER(tmr) < time);
}
/*===========================================================================*/
void bgrt_priv_wait_interval(bgrt_tmr_t * tmr, bgrt_tmr_t time)
{
    bgrt_tmr_t delta;
    BGRT_SET_TIMER(*tmr, time); /* ADLINT:SL:[W0459] does not assign*/
    do
    {
        _BGRT_TMR_YIELD();
        delta = *tmr - BGRT_TIMER(0);         /* ADLINT:SL:[W1052] Unsigned overflow */
    } while ((0 < delta) && (delta <= time)); /* ADLINT:SL:[W0168] Signed/unsigned */
}
