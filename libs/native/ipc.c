#include "ipc.h"

bgrt_st_t bgrt_ipc_init_cs(bgrt_ipc_t * endpoint)
{
    if (!endpoint)
    {
        return BGRT_ST_ENULL;
    }
    BGRT_PRIV_SYNC_INIT(endpoint, BGRT_PRIO_LOWEST);
    endpoint->msg = (void *)0;
    return BGRT_ST_OK;
}

bgrt_st_t bgrt_ipc_init(bgrt_ipc_t * endpoint)
{
    bgrt_st_t ret;
    BGRT_INT_LOCK();
    ret = bgrt_ipc_init_cs(endpoint);
    BGRT_INT_FREE();
    return ret;
}

bgrt_st_t bgrt_ipc_send(bgrt_ipc_t * out, void * msg)
{
    bgrt_st_t ret;
    bgrt_flag_t touch = 0;

    if ((!out) || (!msg))
    {
        return BGRT_ST_ENULL;
    }

    BGRT_PROC_LOCK();

    ret = BGRT_SYNC_SLEEP (out, &touch);

    if (BGRT_ST_OK != ret)
    {
        BGRT_PROC_FREE();
        return ret;
    }

    out->msg = msg;
    ret = BGRT_SYNC_SLEEP(out, &touch);

    BGRT_PROC_FREE();
    return ret;
}

bgrt_st_t bgrt_ipc_wait(bgrt_ipc_t * in, BGRT_PID_T * pid, bgrt_flag_t block)
{
    bgrt_st_t ret;

    if (!in)
    {
        return BGRT_ST_ENULL;
    }

    BGRT_PROC_LOCK();

    ret = BGRT_SYNC_WAIT(in, pid, block);

    if (BGRT_ST_OK != ret)
    {
        goto end;
    }

    ret = BGRT_SYNC_WAKE(in, *pid, 0);

    if (BGRT_ST_OK == ret)
    {
        ret = BGRT_SYNC_WAIT(in, pid, 1);
    }
end:
    BGRT_PROC_FREE();
    return ret;
}

bgrt_st_t bgrt_ipc_reply(bgrt_ipc_t * in, BGRT_PID_T pid)
{
    bgrt_st_t ret;

    if (!in)
    {
        return BGRT_ST_ENULL;
    }

    BGRT_PROC_LOCK();

    ret = BGRT_SYNC_WAKE(in, pid, 0);

    BGRT_PROC_FREE();

    return ret;
}
